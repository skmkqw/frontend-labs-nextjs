const normalizeWord = (word: string) =>
  word.trim().replace(/[^A-ZĄĆĘŁŃÓŚŹŻ]/gi, "").toUpperCase();

const DICTIONARY = new Set(
  RAW_WORDS.split(/\s+/)
    .map(normalizeWord)
    .filter(Boolean),
);

export const isWordValid = (word: string) => DICTIONARY.has(normalizeWord(word));

export const getDictionarySize = () => DICTIONARY.size;

export const getDictionaryWords = () => Array.from(DICTIONARY);

export const addWordToDictionary = (word: string) => {
  const normalized = normalizeWord(word);
  if (normalized) {
    DICTIONARY.add(normalized);
  }
};
